        protected override void LoadContent()
        {
            _spriteBatch = new SpriteBatch(GraphicsDevice);

            // TODO: use this.Content to load your game content here
            _graphics.PreferredBackBufferWidth = SCREEN_WIDTH;
            _graphics.PreferredBackBufferHeight = SCREEN_HEIGHT;
            _graphics.ApplyChanges();
            bounds = new Vector2(_graphics.PreferredBackBufferWidth, _graphics.PreferredBackBufferHeight);

            //Fonts
            basicFont = this.Content.Load<SpriteFont>("fonts/basicFont");

            //Textures
            Texture2D brickTexture = this.Content.Load<Texture2D>("images/bricks");
            Texture2D fpsShootTexture = this.Content.Load<Texture2D>("images/FPS-Shoot-1");
            Texture2D pixelTexture = this.Content.Load<Texture2D>("images/pixel");
            Texture2D darkElfTexture = this.Content.Load<Texture2D>("images/Dark_Elf");

            //Set up the zBuffer with the length of the screen width
            zBuffer = new float[(int)bounds.X];

            collisionHandler = new CollisionHandler(this);
            raycastHandler = new RaycastHandler(this);

            //Walls
            MapLoader mapLoader = new MapLoader(this, map2, brickTexture, 64);
            mapLoader.LoadMap();

            //Player
            IsMouseVisible = false;
            Vector2 playerPos = new Vector2(350, 300);
            Vector2 playerSize = new Vector2(16, 16);
            player = new Player(this, playerPos, playerSize, 0);
            this.Components.Add(player);

            //Raycast Drawer
            raycastDrawer = new RaycastDrawer(this, player, pixelTexture);
            this.Components.Add(raycastDrawer);

            //Ray Sprite Drawer
            raySpriteDrawer = new RaySpriteDrawer(this);
            this.Components.Add(raySpriteDrawer);

            //FPS Gun
            Vector2 fpsGunSize = new Vector2(400, 400);
            Vector2 fpsGunOffset = new Vector2(100, 0);
            Vector2 fpsGunPos = new Vector2(bounds.X / 2 - fpsGunSize.X / 2, bounds.Y - fpsGunSize.Y) + fpsGunOffset;
            fpsGun = new AnimatedSprite(this, fpsShootTexture, fpsGunPos, fpsGunSize, 3, 1, 3);
            fpsGun.CanBeInterpreted = true;
            this.Components.Add(fpsGun);

            RaySprite raySpriteOne = new RaySprite(this, darkElfTexture, new Vector2(500, 500), Color.White);
            this.Components.Add(raySpriteOne);
            this.raySprites.Add(raySpriteOne);

            RaySprite raySpriteTwo = new RaySprite(this, darkElfTexture, new Vector2(700, 500), Color.White);
            this.Components.Add(raySpriteTwo);
            this.raySprites.Add(raySpriteTwo);
        }

        protected override void Update(GameTime gameTime)
        {
            if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed || Keyboard.GetState().IsKeyDown(Keys.Escape))
                Exit();

            // TODO: Add your update logic here
            MouseState ms = Mouse.GetState();
            if (ms.LeftButton == ButtonState.Pressed && ms.LeftButton != oldMouseState.LeftButton)
            {
                fpsGun.Play();
            }
            oldMouseState = ms;

            base.Update(gameTime);
        }

        protected override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.CornflowerBlue);

